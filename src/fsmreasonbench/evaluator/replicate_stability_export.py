"""Run stability analysis and exports for replicate studies (Experiments A & D)."""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

from fsmreasonbench.evaluator.io import dump_json
from fsmreasonbench.experiments.replicate_studies import REPLICATE_METRICS

EXTENSION_DOCS_DIR = "docs/tosem_extension_experiments_v1"
EXTENSION_TABLE_PREFIX = "extension_"


def _latex_escape(text: str) -> str:
    return (
        text.replace("\\", "\\textbackslash{}")
        .replace("_", "\\_")
        .replace("%", "\\%")
        .replace("&", "\\&")
    )


def _load_aggregate(study_root: Path) -> dict[str, Any]:
    path = study_root / "aggregate_replicates.json"
    if not path.exists():
        raise FileNotFoundError(f"missing {path}; run aggregate step first")
    return json.loads(path.read_text(encoding="utf-8"))


def render_replicate_variability_latex(
    aggregate: dict[str, Any],
    *,
    campaign_id: str,
) -> str:
    lines = [
        "% Generated by export_tosem_extension_experiments (read-only; new extension tables).",
        "\\begin{table}[t]",
        "  \\centering",
        f"  \\caption{{Run-to-run variability ({_latex_escape(campaign_id)}). "
        "Mean [95\\% bootstrap CI] across replicates; CV = coefficient of variation.}",
        f"  \\label{{tab:extension-replicate-variability-{_latex_escape(campaign_id.replace('_', '-'))}}}",
        "  \\setlength{\\tabcolsep}{3pt}",
        "  \\begin{tabular}{@{}lllrrrr@{}}",
        "    \\toprule",
        "    Family & Track & Metric & Mean & CV & CI low & CI high \\\\",
        "    \\midrule",
    ]
    for cell in aggregate.get("cells", []):
        family = cell.get("family", "")
        track = cell.get("track", "")
        for metric in REPLICATE_METRICS:
            stats = (cell.get("metrics") or {}).get(metric) or {}
            mean = stats.get("mean")
            if mean is None:
                continue
            cv = stats.get("coefficient_of_variation")
            lo = stats.get("bootstrap_ci_low")
            hi = stats.get("bootstrap_ci_high")
            cv_s = f"{cv:.3f}" if cv is not None else ""
            lines.append(
                f"    {family} & {track} & {metric.replace('_', ' ')} & "
                f"{mean:.3f} & {cv_s} & {lo:.3f} & {hi:.3f} \\\\"
            )
    lines.extend(
        [
            "    \\bottomrule",
            "  \\end{tabular}",
            "\\end{table}",
        ]
    )
    return "\n".join(lines) + "\n"


def build_stability_analysis(aggregate: dict[str, Any]) -> dict[str, Any]:
    """Within-model variance summary and stability diagnostics."""
    cells_out: list[dict[str, Any]] = []
    for cell in aggregate.get("cells", []):
        fully = (cell.get("metrics") or {}).get("fully_correct_rate") or {}
        verdict = (cell.get("metrics") or {}).get("verdict_accuracy") or {}
        cells_out.append(
            {
                "family": cell.get("family"),
                "track": cell.get("track"),
                "temperature": cell.get("temperature"),
                "n_replicates": fully.get("n_replicates", 0),
                "fully_correct_mean": fully.get("mean"),
                "fully_correct_std": fully.get("std"),
                "fully_correct_cv": fully.get("coefficient_of_variation"),
                "fully_correct_ci": [
                    fully.get("bootstrap_ci_low"),
                    fully.get("bootstrap_ci_high"),
                ],
                "verdict_mean": verdict.get("mean"),
                "verdict_std": verdict.get("std"),
                "verdict_cv": verdict.get("coefficient_of_variation"),
            }
        )
    return {
        "experiment": "replicate_stability_analysis",
        "campaign_id": aggregate.get("campaign_id"),
        "study_root": aggregate.get("study_root"),
        "cells": cells_out,
        "interpretation_note": (
            "Compare cross-condition gaps against fully_correct_rate std/CV across replicates "
            "to assess whether observed differences exceed run-to-run variability."
        ),
    }


def _try_stability_plot(study_root: Path, aggregate: dict[str, Any], out_path: Path) -> bool:
    try:
        import matplotlib.pyplot as plt
    except ImportError:
        return False

    labels: list[str] = []
    means: list[float] = []
    err_low: list[float] = []
    err_high: list[float] = []
    for cell in aggregate.get("cells", []):
        stats = (cell.get("metrics") or {}).get("fully_correct_rate") or {}
        mean = stats.get("mean")
        if mean is None:
            continue
        labels.append(f"{cell.get('family')}-{cell.get('track')}")
        means.append(float(mean))
        lo = stats.get("bootstrap_ci_low", mean)
        hi = stats.get("bootstrap_ci_high", mean)
        err_low.append(float(mean) - float(lo))
        err_high.append(float(hi) - float(mean))

    if not labels:
        return False

    fig, ax = plt.subplots(figsize=(8, 4))
    x = range(len(labels))
    ax.errorbar(
        list(x),
        means,
        yerr=[err_low, err_high],
        fmt="o",
        capsize=3,
    )
    ax.set_xticks(list(x), labels, rotation=45, ha="right")
    ax.set_ylabel("Fully correct rate (mean across replicates)")
    ax.set_title(f"Run stability: {aggregate.get('campaign_id', study_root.name)}")
    fig.tight_layout()
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(out_path, bbox_inches="tight")
    plt.close(fig)
    return True


def export_replicate_stability_package(
    repo_root: Path,
    *,
    study_root: Path,
    campaign_id: str,
    paper_tables_dir: Path | None = None,
    paper_figures_dir: Path | None = None,
) -> dict[str, str]:
    aggregate = _load_aggregate(study_root)
    docs_dir = repo_root / EXTENSION_DOCS_DIR
    docs_dir.mkdir(parents=True, exist_ok=True)

    stability = build_stability_analysis(aggregate)
    stability_json = docs_dir / f"replicate_stability_{campaign_id}.json"
    dump_json(stability_json, stability)

    latex = render_replicate_variability_latex(aggregate, campaign_id=campaign_id)
    table_name = f"{EXTENSION_TABLE_PREFIX}replicate_variability_{campaign_id}.tex"
    docs_table = docs_dir / table_name
    docs_table.write_text(latex, encoding="utf-8")

    outputs: dict[str, str] = {
        "stability_json": str(stability_json),
        "variability_table_tex": str(docs_table),
    }

    if paper_tables_dir is not None:
        paper_tables_dir.mkdir(parents=True, exist_ok=True)
        paper_table = paper_tables_dir / table_name
        paper_table.write_text(latex, encoding="utf-8")
        outputs["paper_variability_table"] = str(paper_table)

    if paper_figures_dir is None:
        paper_figures_dir = repo_root.parent / "paper" / "figures"
    fig_path = paper_figures_dir / f"{EXTENSION_TABLE_PREFIX}replicate_stability_{campaign_id}.pdf"
    if _try_stability_plot(study_root, aggregate, fig_path):
        outputs["stability_plot"] = str(fig_path)

    return outputs


def _metric_std_from_aggregate(
    aggregate: dict[str, Any],
    *,
    family: str,
    track: str,
    metric: str = "fully_correct_rate",
) -> float | None:
    for cell in aggregate.get("cells", []):
        if cell.get("family") == family and cell.get("track") == track:
            stats = (cell.get("metrics") or {}).get(metric) or {}
            return stats.get("std")
    return None


def build_stability_vs_cross_model_report(
    repo_root: Path,
    *,
    claude_study_root: Path,
    gpt_study_root: Path,
) -> dict[str, Any]:
    """Compare attribution cross-model gaps against frontier replicate variability (Experiment D)."""
    from fsmreasonbench.evaluator.cross_model_attribution_export import (
        ATTRIBUTION_CONDITIONS,
        build_cross_model_attribution_report,
    )

    cross = build_cross_model_attribution_report(repo_root)
    claude_agg: dict[str, Any] | None = None
    gpt_agg: dict[str, Any] | None = None
    claude_path = claude_study_root / "aggregate_replicates.json"
    gpt_path = gpt_study_root / "aggregate_replicates.json"
    if claude_path.exists():
        claude_agg = json.loads(claude_path.read_text(encoding="utf-8"))
    if gpt_path.exists():
        gpt_agg = json.loads(gpt_path.read_text(encoding="utf-8"))

    noise_floor_f1_r2: dict[str, float | None] = {
        "claude_fully_correct_std": (
            _metric_std_from_aggregate(claude_agg, family="F1", track="R2")
            if claude_agg
            else None
        ),
        "gpt_fully_correct_std": (
            _metric_std_from_aggregate(gpt_agg, family="F1", track="R2")
            if gpt_agg
            else None
        ),
    }
    stds = [
        v
        for v in (
            noise_floor_f1_r2["claude_fully_correct_std"],
            noise_floor_f1_r2["gpt_fully_correct_std"],
        )
        if v is not None
    ]
    replicate_noise_ceiling = max(stds) if stds else None

    rows: list[dict[str, Any]] = []
    paired = {row["condition"]: row for row in cross.get("paired_comparisons") or []}
    for condition in ATTRIBUTION_CONDITIONS:
        pair = paired.get(condition, {})
        delta = pair.get("gpt_minus_claude_cert_rate")
        abs_delta = abs(float(delta)) if delta is not None else None
        exceeds = (
            abs_delta is not None
            and replicate_noise_ceiling is not None
            and abs_delta > replicate_noise_ceiling
        )
        rows.append(
            {
                "condition": condition,
                "status": pair.get("status", "pending"),
                "abs_gpt_minus_claude_cert_rate": abs_delta,
                "replicate_noise_ceiling_f1_r2_std": replicate_noise_ceiling,
                "difference_exceeds_replicate_variability": exceeds,
            }
        )

    return {
        "experiment": "run_stability_vs_cross_model_comparison",
        "noise_floor": noise_floor_f1_r2,
        "replicate_noise_ceiling_f1_r2_std": replicate_noise_ceiling,
        "interpretation_note": (
            "If abs(GPT-Claude certificate gap) exceeds the larger F1 R2 fully_correct "
            "replicate std from frontier tool campaigns, the cross-model gap likely "
            "exceeds run-to-run variability at the same protocol."
        ),
        "conditions": rows,
        "replicate_studies": {
            "claude": str(claude_study_root),
            "gpt": str(gpt_study_root),
        },
    }


def render_stability_vs_cross_model_latex(report: dict[str, Any]) -> str:
    lines = [
        "% Generated by export_tosem_extension_experiments (Experiment D).",
        "\\begin{table}[t]",
        "  \\centering",
        "  \\caption{Attribution cross-model gaps vs run-to-run variability (F1 R2 frontier replicates).}",
        "  \\label{tab:extension-stability-vs-cross-model}",
        "  \\begin{tabular}{@{}lrrl@{}}",
        "    \\toprule",
        "    Condition & $|\\Delta|$ cert. & Replicate std ceiling & Exceeds variability? \\\\",
        "    \\midrule",
    ]
    ceiling = report.get("replicate_noise_ceiling_f1_r2_std")
    ceiling_s = f"{ceiling:.3f}" if ceiling is not None else ""
    for row in report.get("conditions") or []:
        delta = row.get("abs_gpt_minus_claude_cert_rate")
        delta_s = f"{delta:.3f}" if delta is not None else ""
        if row.get("status") != "available":
            exceeds_s = "pending"
        elif row.get("difference_exceeds_replicate_variability") is True:
            exceeds_s = "yes"
        elif row.get("difference_exceeds_replicate_variability") is False:
            exceeds_s = "no"
        else:
            exceeds_s = ""
        lines.append(
            f"    {row.get('condition')} & {delta_s} & {ceiling_s} & {exceeds_s} \\\\"
        )
    lines.extend(["    \\bottomrule", "  \\end{tabular}", "\\end{table}"])
    return "\n".join(lines) + "\n"


def export_run_stability_comparison_package(
    repo_root: Path,
    *,
    claude_study_root: Path,
    gpt_study_root: Path,
    paper_tables_dir: Path | None = None,
) -> dict[str, str]:
    report = build_stability_vs_cross_model_report(
        repo_root,
        claude_study_root=claude_study_root,
        gpt_study_root=gpt_study_root,
    )
    docs_dir = repo_root / EXTENSION_DOCS_DIR
    docs_dir.mkdir(parents=True, exist_ok=True)
    json_path = docs_dir / "run_stability_vs_cross_model.json"
    dump_json(json_path, report)
    latex = render_stability_vs_cross_model_latex(report)
    table_name = f"{EXTENSION_TABLE_PREFIX}stability_vs_cross_model.tex"
    docs_table = docs_dir / table_name
    docs_table.write_text(latex, encoding="utf-8")
    outputs = {
        "stability_vs_cross_model_json": str(json_path),
        "stability_vs_cross_model_table_tex": str(docs_table),
    }
    if paper_tables_dir is not None:
        paper_tables_dir.mkdir(parents=True, exist_ok=True)
        paper_table = paper_tables_dir / table_name
        paper_table.write_text(latex, encoding="utf-8")
        outputs["paper_stability_vs_cross_model_table"] = str(paper_table)
    return outputs
